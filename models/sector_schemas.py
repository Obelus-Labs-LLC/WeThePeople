    filing_count: Optional[int] = None
    lobbying_count: int = 0
    contract_count: int = 0
    total_contract_value: float = 0
    enforcement_count: int = 0
    total_penalties: float = 0
    latest_stock: Optional[StockSnapshot] = None

    class Config:
        extra = "allow"


