"""
Pydantic response models for sector factory endpoints.

These models are used as response_model on the 14 generic endpoints
generated by create_sector_router(). They document the exact response
shape in OpenAPI/Swagger and provide runtime validation.
"""

from __future__ import annotations

from typing import Any, Dict, List, Optional

from pydantic import BaseModel


# ── Dashboard ───────────────────────────────────────────────────────────────


class SectorDashboardStats(BaseModel):
    """Generic sector dashboard stats. Extra keys allowed per sector."""
    total_lobbying: int = 0
    total_lobbying_spend: float = 0
    total_contracts: int = 0
    total_contract_value: float = 0
    total_enforcement: int = 0
    total_penalties: float = 0
    by_sector: Dict[str, int] = {}

    class Config:
        extra = "allow"


class ActivityMeta(BaseModel):
    class Config:
        extra = "allow"


class RecentActivityItem(BaseModel):
    type: str
    title: Optional[str] = None
    description: Optional[str] = None
    date: Optional[str] = None
    company_name: Optional[str] = None
    url: Optional[str] = None
    meta: Optional[Dict[str, Any]] = None

    class Config:
        extra = "allow"


class RecentActivityResponse(BaseModel):
    items: List[RecentActivityItem]


